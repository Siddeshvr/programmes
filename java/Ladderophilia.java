import java.util.Scanner;

class Ladderophilia
{
    public static void main(String[] args)
    {
        Scanner jin = new Scanner(System.in);
        int n = jin.nextInt();
        jin.close();
        for(int i=0;i<n;i++)
        {
            System.out.println();
            System.out.println("*   *");
            System.out.println();
            System.out.println("*   *");
            System.out.println();
            System.out.println("*****");
        }
        System.out.println();
        System.out.println("*   *");
        System.out.println();
        System.out.println("*   *");
    }
}